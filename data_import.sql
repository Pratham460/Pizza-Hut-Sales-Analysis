-- DATA IMPORT PROCESS

-- The datasets used in this project were provided as CSV files.

-- These files were imported into MySQL using MySQL Workbench's
-- "Table Data Import Wizard".

-- Steps followed:

-- 1. Open MySQL Workbench
-- 2. Select the target database schema
-- 3. Right-click and choose "Table Data Import Wizard"
-- 4. Select the CSV file
-- 5. MySQL Workbench automatically creates a table based on the file structure
-- 6. Import the data into the database

-- Files imported:

-- order_details.csv  -> order_details table
-- orders.csv     -> orders table
-- pizza_types.csv   -> pizza_types table
-- pizzas.csv -> pizzas table

-- After importing the datasets, SQL queries were executed
-- to perform data analysis and generate insights.